                    <h2>Speaker Location</h2>
                    <p>
                      Below is a list of helpful tips when choosing a location for your speaker system. These tips are meant to supplement your product's Quick Start Guide only. QSG's can be downloaded in the <a href="../literature">Literature</a> section of this website.
                    </p>
                    <ul class="support-list">
                      <li>Rooms will have power in one or many locations, in the ceiling or down in the room. Choose a location for the speaker where power is accessible by the power supply (6 reach). If required, we offer an assortment of long run power extension cables for up to 40' runs.</li>

                      <li><b>Astronaut:</b> Generally, the Astronaut is installed near a projector on the ceiling for easy source connection and power access to an in-ceiling power outlet.</li>

                      <li><b>Ensemble:</b> The Ensemble is a portable system. In some cases, the best place for the speaker is near audience members where there are no power outlets. In this instance, long run power cables are required.</li>

                      <li>When using a microphone, avoid feedback by placing the speaker far from where the transmitter will be used while close to the audience members.</li>

                      <li>Always consider the location of multimedia devices when installing or setting up an audio system. Make sure you have the appropriate length and type of speaker cables to accommodate your installation.</li>

                      <li>Place speakers near the intended audience since sound levels decrease quickly over large distances. For the Ens√©mble, ensure the intended audience is within a 40' radius of the speaker.</li>

                      <li>Cetacea's patented speaker designs are very forgiving for placement because of their omni-directional acoustic horn design. This means all audience members benefit from hearing the same sound regardless of where they are seated around the speaker. However, if possible, locate speakers at least 4 feet away from vertical walls.</li>

                      <li><b>Caution:</b> Do not locate any ceiling speakers directly over or within 3' of the hot air created by projector exhausts.</li>
                    </ul>
                  </article>

                  <article id="source-connections">
                    <h2>Source Connections</h2>
                    <p>
                      All Cetacea speakers can be hooked up to a variety of different A/V devices using stereo or mono speaker cables with 3.5 mm mini jacks. Common devices include: projectors, DVD/VCR's, microphones, whiteboards, and portable music players (iPod&reg;). Refer to your product's Quick Start Guide for input specifications and hookup diagrams.
                    </p>
                    <ul class="support-list">
                      <li>The Astronaut and Ensemble speakers have a built-in 3-channel mixer which accepts up to 3 simultaneous devices (A, B, C channels).</li>

                      <li>Do not connect 3rd party amplifiers to Cetacea powered speakers as they could overdrive the speakers, causing distortion or premature failure.</li>

                      <li>Most fixed and variable line outputs are suitable sources. If distortion or other audible noise is detected, make sure the source is functioning and any applicable software is set up correctly.</li>

                      <li>Stereo connections: Max input voltage should not exceed 300mv per input channel.</li>

                      <li>Mono connections: Max input voltage should not exceed 150mv per input channel. Cetacea speakers require line level, not microphone level inputs.</li>

                      <li>When connecting to a fixed line output, a separate attenuator must be purchased to adjust volume. There is no remote volume control for the Ensemble or Astronaut products.</li>

                      <li>Always plug AC power in last after plugging in all source cables.</li>

                      <li>Observe all audio 101 installation standards when routing cables across a ceiling. For instance, do not lay source cables parallel to power cables or place them too close to fluorescent light ballasts. This may cause unwanted noise or hum.</li>
                    </ul>
                  </article>

                  <article id="power-considerations">
                    <h2>Power Considerations</h2>
                    <p>
                      All Cetacea audio systems include an external power supply. There are a few advantages to this: they save weight; allow us to upgrade as new technology becomes available; and, we can offer various styles for different installation needs.
                    </p>
                    <ul class="support-list">
                      <li>Desktop style power supplies are handy for swapping out different length AC power cords. Additionally, the plugs are small and do not take up as much space in a power outlet as a wall mount power supply brick.</li>

                      <li>Some installations may require plenum-rated power cables. Visit the <a href="../../cabling-power-options">power and cabling section</a> of this website for plenum-rated cabling options.</li>

                      <li>Cetacea speakers can handle long run power extensions of up to 40'. Check out our line of AC and DC <a href="../../cabling-power-options">power extension cables</a>.</li>

                      <li><b>Tip:</b> In the case that your power supply fails, please recycle it according to local laws. Older supplies can be returned to use for a 25% discount on a new ENERGY-STAR&reg; model.</li>
                    </ul>
