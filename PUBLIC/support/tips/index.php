                  <article id="grounding">
                    <h2>Grounding</h2>
                    <p>
                      Cetacea Sound speakers can and often are affected by inappropriate electrical grounding. Although all current Cetacea Sound power supply models are isolated from ground, problems can still arise during installation.
                    </p>
                    <p>
                      Do not assume that proper safety grounding and electronic noise reduction are one and the same. The building electrical contractor is rarely asked to do the latter which exposes every audio installation to ground noise both on the power line as well as the signal line.
                    </p>
                    <p>
                      Cetacea Sound powered speakers have built in low and high voltage, thermal, and diode protection for polarity; however, ground noise can still be caused from ground loops within projectors, DVDs, and computers plugged into the same circuit. If you hear a 60 cycle hum from the speaker, try plugging it into another AC outlet either in the room or across the hall using an extension cord. If the hum is eliminated, then a different source of AC power will have to be used or an electrical contractor will have to provide a common ground for all equipment on the circuit in question.
                    </p>
                    <p>
                      Ground noise, hiss, and distortion can be caused on the signal line if the signal wire is connected to an improperly designed source. Projectors, personal computers, wall plates, and other devices are all suspect if it is determined this is the case.
                    </p>
                    <p>
                      To diagnose the noise source, first disconnect the power cord at the speaker. Second, disconnect the signal wire from the speaker. Connect another source to the speaker like a personal computer, portable CD player, or iPod&reg;. Finally, reconnect the power cord. If the noise was eliminated, then the first signal source is suspect.
                    </p>
                    <p>
                      Systematically remove each source of the signal path one by one until the culprit is discovered. Once the source with the noise is discovered, find its fault through another process of elimination. Some signal sources just don't work the way they are supposed to and mislead one to think the speaker has a problem, which in fact it does not.
                    </p>
                  </article>

                  <article id="noise">
                    <h2>Noise &amp; Distortion</h2>
                    <p>
                      Do not assume that noise in the form of hum, static, pops, or distortion is the fault of the speaker. Noise is most commonly caused by multimedia sources or power issued within an installation. Here are some of the most common causes to consider:
                    </p>
                    <ul class="support-list">
                      <li>Ground loop AC hum, 60 Hz (see <a href="#grounding">grounding tips</a>)</li>

                      <li>Bad sources like improperly grounded portable computer docking stations</li>

                      <li>Ungrounded or improperly grounded wall switch plates.</li>

                      <li>Bad cables; especially custom cables</li>

                      <li>Bad MP3 files or corrupted audio files from the internet, CD, or computer</li>

                      <li>Volume turned up too high (properly designed speakers should sound distorted at maximum volume)</li>

                      <li>Distortion is commonly caused by an incorrectly set up source. Deactivate, disable, and under no circumstances use equalizers or compression algorithms that are resident in most source player setup menus. These software controls can be easily identified as "Bass Boost", "Cinema Sound ", "Q-Sound", "Surround Sound", "Brightness", "Rock and Roll", "Blues", etc.</li>

                      <li>For the Astronaut: Source cables must be terminated on both ends (one end at the amplifier and one end at the source); otherwise, noise will be experienced</li>
                    </ul>
                    <p>
                      <span>Rule of Thumb</span>The larger the number of connections in a source path, the more likely that noise will transfer to the speaker. Each and every source and connection is a potential cause of noise and must be investigated through a process of elimination.
                    </p>
                    <p>
                      Cetacea Sound speakers are very easy to diagnose. For additional support, refer to our <a href="../troubleshoot">Troubleshooting</a> section.
                    </p>
                  </article>

                  <article id="microphone-use">
                    <h2>Microphone Use</h2>
                    <p>
                      <span>Practice Makes Perfect</span>Learning to use a wireless microphone is like learning to drive a car‚Äîpractice, practice, practice! Always prepare for a presentation by setting up your equipment in the room you will be speaking in beforehand. Make sure the equipment is working properly and then walk around the room.
                    </p>
                    <p>
                      Spend time practicing in the area of the room where you will be speaking. Listen for any unwarranted noise or feedback as you walk the room and avoid those noisy areas that cannot be eliminated by equipment adjustments.
                    </p>
                    <p>
                      Have a second person help you hear and make volume adjustments. In most cases, the appropriate microphone volume is when the presenter cannot hear themselves over the speakers.
                    </p>
                  </article>

                  <article id="feedback">
                    <h2>Microphone Feedback</h2>
                    <p>
                      Microphones will experience feedback unless used with care. Feedback is caused by a variety of factors, including speaker and microphone placement, the room environment, and volume.
                    </p>
                    <p>
                      <span>Volume</span>Set the appropriate volume level for your room with the help of a second person. High microphone volume is a common cause for feedback.
                    </p>
                    <ol class="support-list">
                      <li>Stand as far away from the speaker as possible but close to the audience and adjust volume until it is loud enough for the audience to hear.</li>
                      <li>Have a second person determine appropriate loudness by standing or sitting in the audience location.</li>
                      <li>Lower volume if any sign of feedback is detected.</li>
                    </ol>
                    <p class="notice">
                      <span>Note:</span>The appropriate audience volume level may sound low to the person with the transmitter.
                    </p>
                    <p>
                      <span>Speaker Placement</span>Microphone proximity to the speaker is a common cause of feedback. Speakers can easily be relocated with extension cables. <a href="../../about/contact">Contact us</a> for an extension cable kit.
                    </p>
                    <p>
                      <span>Room Environment</span>Always walk the room to detect sensitive feedback areas. In some cases, these areas will be unavoidable due to the unique properties of the room. Avoid these areas during microphone use.
                    </p>
