                  <article>
                    <h2>Speaker Diagnostic</h2>
                    <p>
                      This guide covers the most common issues encountered with powered speakers. Perform this self-diagnostic procedure prior to contacting us; it will help us further diagnose any problem you are experiencing. These instructions are written specifically for Cetacea powered speakers, including the Ensemble, PAS, and Astronaut branded speakers.
                    </p>
                    <p>
                      Common speaker problems result from:
                    </p>
                    <ul>
                      <li>Poor connections</li>
                      <li>Poor audio sources</li>
                      <li>Incorrect or unapproved power supplies</li>
                    </ul>

                    <h3>Connections</h3>
                    <p>
                      Always refer to the connection diagrams provided in your product’s Quick Start Guide (found in the literature section of this website). In addition, some diagrams have been made available in the product sections of this website.
                    </p>
                    <p>
                      Common speaker problems result from:
                    </p>
                    <ol class="support-list">
                      <li>Please ensure all connections are made using high-quality speaker or power cables, are tight when connected, and that all connector plugs are clean without any visible corrosion.</li>

                      <li>Never force a source connector into a speaker input; if it requires force, it is either the wrong connector plug or wrong input (Cetacea speakers use 3.5 mm input jacks).</li>

                      <li>Make sure source connections are made tightly and prior to connecting the power supply in the following step.</li>

                      <li>Make sure the power supply is fully plugged into the power outlet and correct speaker terminal. In some cases, Cetacea speakers have an LED to indicate power (Astronaut CL / XL).</li>

                      <li>If noise, distortion, no sound, or any other problem persists after checking these connections, continue to the next section: "Source Devices".</li>
                    </ol>

                    <h3>Source Devices</h3>
                    <p>
                      Cetacea speakers are amplified and designed to plug into either mono or stereo sources of sound like projectors, microphones, CD / DVD / VHS players, interactive whiteboards, and iPod&reg;'s. Cetacea speakers are not designed to plug into third party amplifiers, pre-amps, or subwoofer outputs. Most line and variable headphone jacks are suitable sources.
                    </p>
                    <ol class="support-list">
                      <li>To determine whether a source device is causing the problem, first isolate it by testing multiple audio sources. For example, if a computer, projector, and iPod&reg; are your intended sources, plug the devices into the speaker, one by one, to check for sound.</li>

                      <li>If you hear sound with one source device but not another, then the sound source is either incorrectly set up, connected to the wrong source output, or faulty. Check your source's documentation for further instructions.</li>

                      <li><em>Computers:</em> In many cases, if the computer is not playing any sound, then the headphone or line-out jack is not set up correctly in the computer soundcard software or is muted. Also, please ensure that the computer volume is set to at least 50%.</li>

                      <li>If noise, distortion, no sound, or any other problem persists for all tested source devices, you may have a faulty power supply. Continue to the next section: "Power Supplies".</li>
                    </ol>

                    <h3>Power Supplies</h3>
                    <p>
                      In rare instances, a power supply could be faulty. All Cetacea speakers come with an external power supply designed specifically for your system. Any use of another manufacturer’s power supply may cause undesirable results, including: excessive noise; weak sound levels; or, distortion. Every Cetacea power supply has a label with Cetacea branding and its SKU number.
                    </p>
                    <ol class="support-list">
                      <li>If your power supply is not Cetacea branded, it is very likely that it is the source of your problem. Please contact us for a replacement.</li>

                      <li>If noise, distortion, no sound, or any other problem continues to persist, then contact Technical Support for further diagnostics. Please make sure your equipment is available and accessible to you to test while on the phone. We will not be able to support you without the equipment available to diagnose over the phone.</li>
                    </ol>
